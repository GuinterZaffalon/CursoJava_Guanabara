    private void botaoFormaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoFormaActionPerformed
        // TODO add your handling code here:
        int a = slide1.getValue();
        int b = slider2.getValue();
        int c = slider3.getValue();
        
        if (a<b+c && b<a+c && c<b+c) {
            resultadoLabel.setText("Formam um triangulo");
            if (a==b && b==c) {
                tipoTriangulo.setText("Equilatero");
            } else if (a != b && b != c && a!= c) {
                tipoTriangulo.setText("Escaleno");
            } else {
                tipoTriangulo.setText("Isoceles");
            }
        } else {
            resultadoLabel.setText("NÃ£o formam um triangulo");
            tipoTriangulo.setText("------");
        }
        resultadoLabel.setVisible(true);
    }//GEN-LAST:event_botaoFormaActionPerformed

